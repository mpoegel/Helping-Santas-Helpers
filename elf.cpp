#include "elf.h"

unsigned int Elf::elf_count_ = 1;
// -----------------------------------------------------------------------------
// CONSTRUCTORS
// -----------------------------------------------------------------------------
Elf::Elf() {
	id_ = elf_count_;
	elf_count_++;
	productivity_ = 1.0;
	is_working_ = false;
	current_toy_ = NULL;
}

// -----------------------------------------------------------------------------
// ACCESSORS
// -----------------------------------------------------------------------------

// -----------------------------------------------------------------------------
// MODIFIERS
// -----------------------------------------------------------------------------
bool Elf::assignToy(Toy* t) {
	if (is_working_) {
		return false;
	}
	current_toy_ = t;
	is_working_ = true;
	return true;
}

